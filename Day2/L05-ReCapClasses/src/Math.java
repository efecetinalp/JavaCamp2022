public class Math {
    public int Sum(int number1, int number2) {
        return number1 + number2;
    }

    public int Substract(int number1, int number2) {
        return number1 - number2;
    }

    public int Multiply(int number1, int number2) {
        return number1 * number2;
    }

    public int Divide(int number1, int number2) {
        return number1 / number2;
    }
}
